DATA SEGMENT
  TABLE	DB  30H,31H,32H,33H,34H,35H,36H,37H
        DB  38H,39H,41H,42H,43H,44H,45H,46H
  HEX   DB  03H  ;设置你想要的转换的16进制数
  ASCLL DB  0    ;用于保存转换之后的值
DATA ENDS

STACK SEGMENT STACK
	  DW 20H DUP(0)
STACK ENDS

CODE SEGMENT
  ASSUME DS:DATA,CS:CODE,ss:stack
START:
  MOV AX,DATA
  MOV DS,AX
  
  LEA SI,TABLE
  MOV AL,HEX
  MOV BX,SI
  XLAT
  MOV ASCLL,AL
  
  
  
  MOV AH,4CH
  INT 21H
CODE ENDS
    END START